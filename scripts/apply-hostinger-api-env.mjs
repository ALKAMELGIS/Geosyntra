function parseEnvFile(filePath) {
  const out = {}
  if (!fs.existsSync(filePath)) return out
  for (const line of fs.readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq <= 0) continue
    const key = trimmed.slice(0, eq).trim()
    let value = trimmed.slice(eq + 1).trim()
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    out[key] = value
  }
  return out
}

function resolveMapboxToken(vars) {
  return (
    String(vars.MAPBOX_TOKEN || '').trim() ||
    String(vars.MAPBOX || '').trim() ||
    String(vars.MAPBOX_ACCESS_TOKEN || '').trim()
  )
}

function buildBackendEnv(vars) {
  const lines = [
    '# Generated by scripts/apply-hostinger-api-env.mjs — do not commit.',
    `# ${new Date().toISOString()}`,
    '',
  ]
  const mapbox = resolveMapboxToken(vars)
  const written = new Set()
  for (const key of SERVER_KEYS) {
    if (key === 'MAPBOX' || key === 'MAPBOX_ACCESS_TOKEN') continue
    if (key === 'MAPBOX_TOKEN') {
      if (mapbox) {
        lines.push(`MAPBOX_TOKEN=${mapbox}`)
        written.add('MAPBOX_TOKEN')
      }
      continue
    }
    const value = String(vars[key] ?? '').trim()
    if (!value) continue
    lines.push(`${key}=${value}`)
    written.add(key)
  }
  if (!vars.AGRI_USER_DB_PATH && vars.AGRI_DATA_DIR) {
    lines.push(`AGRI_USER_DB_PATH=${path.posix.join(vars.AGRI_DATA_DIR, 'geosyntra_platform.db')}`)
  }
  return `${lines.join('\n')}\n`
}

async function checkMapboxConfig(apiBase) {
  const url = `${apiBase.replace(/\/+$/, '')}/api/config/mapbox`
  try {
    const res = await fetch(url, { headers: { Accept: 'application/json' } })
    const data = await res.json().catch(() => ({}))
    console.log(`\nGET ${url}`)
    console.log(`HTTP ${res.status}`)
    const safe = {
      ...data,
      publicToken: data.publicToken ? `${String(data.publicToken).slice(0, 8)}…` : null,
    }
    console.log(JSON.stringify(safe, null, 2))
    if (res.ok && data.configured && data.publicToken) {
      console.log('\nOK: Mapbox configured — public pk.* available for Mapbox GL init.')
      return true
    }
    if (res.ok && data.configured) {
      console.log('\nWARN: Mapbox configured but publicToken missing — use pk.* in MAPBOX_TOKEN.')
      return false
    }
    console.log('\nWARN: Mapbox not configured on API host (set MAPBOX_TOKEN in hPanel → Node.js env).')
    return false
  } catch (e) {
    console.error(`\nFAIL: Cannot reach Mapbox config at ${url}`)
    console.error(String(e?.message || e))
    return false
  }
}

async function checkRemote(apiBase) {
  const oauthUrl = `${apiBase.replace(/\/+$/, '')}/api/auth/oauth/config`
  try {
    const res = await fetch(oauthUrl, { credentials: 'include' })
    const data = await res.json().catch(() => ({}))
    console.log(`GET ${oauthUrl}`)
    console.log(`HTTP ${res.status}`)
    console.log(JSON.stringify(data, null, 2))
    if (data?.google?.configured && data?.google?.clientId) {
      console.log('\nOK: Google OAuth is exposed by the API.')
    } else {
      console.log('\nWARN: API reachable but Google OAuth not configured (check GOOGLE_OAUTH_* on server).')
    }
  } catch (e) {
    console.error(`\nFAIL: Cannot reach API at ${oauthUrl}`)
    console.error(String(e?.message || e))
    console.error('Set DNS A record api.geosyntra.org → VPS IP and ensure Node listens on port 3001.')
  }
  await checkMapboxConfig(apiBase)
}

function sshUploadWithScp(vars) {
  const host = String(vars.HOSTINGER_SSH_HOST || '').trim()
  const user = String(vars.HOSTINGER_SSH_USER || '').trim()
  const port = String(vars.HOSTINGER_SSH_PORT || '22').trim()
  const appDir = String(vars.HOSTINGER_APP_DIR || '').trim()
  const pm2Name = String(vars.HOSTINGER_PM2_NAME || 'geosyntra-api').trim()
  if (!host || !user || !appDir) {
    console.log('SSH upload skipped (set HOSTINGER_SSH_HOST, HOSTINGER_SSH_USER, HOSTINGER_APP_DIR in hostinger.secrets.env).')
    return false
  }

  const remoteEnv = `${appDir}/backend/.env`
  const target = `${user}@${host}:${remoteEnv}`
  console.log(`Uploading backend/.env → ${target}`)
  const scp = spawnSync(
    'scp',
    ['-P', port, '-o', 'BatchMode=yes', '-o', 'ConnectTimeout=15', BACKEND_ENV, target],
    { stdio: 'inherit', shell: process.platform === 'win32' },
  )
  if (scp.status !== 0) {
    console.error('scp failed — set HOSTINGER_SSH_PASS and install ssh2, or paste env in hPanel → Node.js.')
    return false
  }

  console.log(`Restarting PM2 app "${pm2Name}"…`)
  const sshCmd = `cd ${appDir} && pm2 restart ${pm2Name} || (cd backend && npm run start)`
  spawnSync('ssh', ['-p', port, '-o', 'BatchMode=yes', '-o', 'ConnectTimeout=15', `${user}@${host}`, sshCmd], {
    stdio: 'inherit',
    shell: process.platform === 'win32',
  })
  return true
}

async function sshUploadWithPassword(vars) {
  const pass = String(vars.HOSTINGER_SSH_PASS || '').trim()
  if (!pass) return false

  let Client
  try {
    ;({ Client } = await import('ssh2'))
  } catch {
    console.warn('ssh2 not installed — run: npm i -D ssh2 (for password-based SSH upload on Windows)')
    return false
  }

  const host = String(vars.HOSTINGER_SSH_HOST || '').trim()
  const user = String(vars.HOSTINGER_SSH_USER || '').trim()
  const port = Number(vars.HOSTINGER_SSH_PORT || 65002)
  const appDir = String(vars.HOSTINGER_APP_DIR || '').trim()
  const pm2Name = String(vars.HOSTINGER_PM2_NAME || 'geosyntra-api').trim()
  const remoteEnv = `${appDir}/backend/.env`
  const body = fs.readFileSync(BACKEND_ENV)

  return new Promise(resolve => {
    const conn = new Client()
    conn
      .on('ready', () => {
        conn.sftp((err, sftp) => {
          if (err) {
            console.error('SFTP failed:', err.message)
            conn.end()
            resolve(false)
            return
          }
          const stream = sftp.createWriteStream(remoteEnv)
          stream.on('close', () => {
            console.log(`Uploaded ${remoteEnv} via SFTP (ssh2)`)
            conn.exec(`cd ${appDir} && pm2 restart ${pm2Name} || (cd backend && npm run start)`, (e, ch) => {
              if (e) {
                console.error('PM2 restart failed:', e.message)
                conn.end()
                resolve(false)
                return
              }
              ch.on('close', () => {
                conn.end()
                resolve(true)
              })
              ch.stderr.on('data', d => process.stderr.write(d))
              ch.stdout.on('data', d => process.stdout.write(d))
            })
          })
          stream.on('error', e => {
            console.error('SFTP write failed:', e.message)
            conn.end()
            resolve(false)
          })
          stream.end(body)
        })
      })
      .on('error', e => {
        console.error('SSH connection failed:', e.message)
        resolve(false)
      })
      .connect({ host, port, username: user, password: pass, readyTimeout: 20000 })
  })
}

async function sshUpload(vars) {
  const pass = String(vars.HOSTINGER_SSH_PASS || '').trim()
  if (pass) {
    if (await sshUploadWithPassword(vars)) return
  } else {
    console.log('HOSTINGER_SSH_PASS not set — skipping password SSH. Use hPanel env vars or set HOSTINGER_SSH_PASS + npm i -D ssh2.')
  }
  sshUploadWithScp(vars)
}

async function main() {
  const checkOnly = process.argv.includes('--check')
  const vars = parseEnvFile(SECRETS_FILE)

  if (!fs.existsSync(SECRETS_FILE)) {
    console.error(`Missing ${SECRETS_FILE}`)
    console.error(`Copy hostinger.secrets.env.example → hostinger.secrets.env and fill MAPBOX_TOKEN + OAuth/SMTP.`)
    process.exit(1)
  }

  const apiBase = String(vars.VITE_API_BASE_URL || 'https://api.geosyntra.org').trim()
  if (checkOnly) {
    await checkRemote(apiBase)
    return
  }

  const mapbox = resolveMapboxToken(vars)
  if (!mapbox) {
    console.warn('WARN: MAPBOX_TOKEN empty — Mapbox basemaps will not work until you set pk.* in hostinger.secrets.env or hPanel.')
  } else if (!mapbox.startsWith('pk.')) {
    console.warn('WARN: MAPBOX_TOKEN should be a public pk.* token for Mapbox GL init in the browser.')
  }

  const hasGoogle =
    String(vars.GOOGLE_OAUTH_CLIENT_ID || '').trim() &&
    String(vars.GOOGLE_OAUTH_CLIENT_SECRET || '').trim()

  if (!hasGoogle) {
    console.warn('WARN: GOOGLE_OAUTH_CLIENT_ID/SECRET empty — backend/.env will omit Google until you fill hostinger.secrets.env')
  }

  fs.writeFileSync(BACKEND_ENV, buildBackendEnv(vars), 'utf8')
  console.log(`Wrote ${BACKEND_ENV}`)

  await sshUpload(vars)

  console.log('\nNext steps:')
  console.log('1. Prefer hPanel → Node.js → Environment Variables (MAPBOX_TOKEN persists across git redeploys)')
  console.log('2. GitHub → Settings → Secrets → VITE_API_BASE_URL =', apiBase)
  console.log('3. node scripts/apply-hostinger-api-env.mjs --check')
  console.log('4. Google Console redirect URI:', vars.GOOGLE_OAUTH_REDIRECT_URI || 'https://www.geosyntra.org/oauth-return.html')
  if (vars.HOSTINGER_SERVER_IP) {
    console.log('5. DNS: A record api.geosyntra.org →', vars.HOSTINGER_SERVER_IP)
  }
}

main().catch(e => {
  console.error(e)
  process.exit(1)
})
